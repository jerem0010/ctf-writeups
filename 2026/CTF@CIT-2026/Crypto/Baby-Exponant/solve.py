def integer_cube_root(x):
    low = 0
    high = x

    while low <= high:
        mid = (low + high) // 2
        cube = mid ** 3

        if cube == x:
            return mid, True
        elif cube < x:
            low = mid + 1
        else:
            high = mid - 1

    return high, False


